// PascalCasing - Every first letter of a word is in capital.
// Function components in react should always follow PascalCasing.
function Message() {
  // This is a JSX:Javascript XML code.
  const name = "";
  if (name) {
    return <h1>{name}</h1>;
  } else {
    return <h1>Hello World</h1>;
  }
}

export default Message;
